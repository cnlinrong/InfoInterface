package cn.cemobile.infointerface.model;

public class RequestInfo {

	private String userid = "";
	private String title = "";
	private String content = "";
	private String province = "";
	private String city = "";
	private String casetype = "";
	private String status = "";
	private String lawerid = "";
	private String istip = "";
	private String pageno = "";
	private String pagesize = "";

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCasetype() {
		return casetype;
	}

	public void setCasetype(String casetype) {
		this.casetype = casetype;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLawerid() {
		return lawerid;
	}

	public void setLawerid(String lawerid) {
		this.lawerid = lawerid;
	}

	public String getIstip() {
		return istip;
	}

	public void setIstip(String istip) {
		this.istip = istip;
	}

	public String getPageno() {
		return pageno;
	}

	public void setPageno(String pageno) {
		this.pageno = pageno;
	}

	public String getPagesize() {
		return pagesize;
	}

	public void setPagesize(String pagesize) {
		this.pagesize = pagesize;
	}

}
